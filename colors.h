#ifndef COLORS_H
# define COLORS_H

# define RESET			"\u001b[0m"
# define BOLD			"\u001b[1m"
# define UNDERLINE		"\u001b[4m"
# define REVERSED		"\u001b[7m"

/*
** Regular colors
*/

# define BLACK			"\u001b[30m"
# define RED			"\u001b[31m"
# define GREEN			"\u001b[32m"
# define YELLOW			"\u001b[33m"
# define BLUE			"\u001b[34m"
# define MAGENTA		"\u001b[35m"
# define CYAN			"\u001b[36m"
# define WHITE			"\u001b[37m"

/*
** Bright colors
*/

# define BBLACK			"\u001b[30;1m"
# define BRED			"\u001b[31;1m"
# define BGREEN			"\u001b[32;1m"
# define BYELLOW		"\u001b[33;1m"
# define BBLUE			"\u001b[34;1m"
# define BMAGNETA		"\u001b[35;1m"
# define BCYAN			"\u001b[36;1m"
# define BWHITE			"\u001b[37;1m"

/*
** Background regular colors
*/

# define BACK_BLACK		"\u001b[40m"
# define BACK_RED		"\u001b[41m"
# define BACK_GREEN		"\u001b[42m"
# define BACK_YEL		"\u001b[43m"
# define BACK_BLUE		"\u001b[44m"
# define BACK_MAG		"\u001b[45m"
# define BACK_CYAN		"\u001b[46m"
# define BACK_WHITE		"\u001b[47m"

/*
** Background bright colors
*/

# define BACK_BBLACK	"\u001b[40;1m"
# define BACK_BRED		"\u001b[41;1m"
# define BACK_BGREEN	"\u001b[42;1m"
# define BACK_BYEL		"\u001b[43;1m"
# define BACK_BBLUE		"\u001b[44;1m"
# define BACK_BMAG		"\u001b[45;1m"
# define BACK_BCYAN		"\u001b[46;1m"
# define BACK_BWHITE	"\u001b[47;1m"

#endif
